def string_upper(world):
    """Функция принимает на вход строку и возвращает 
       заглавными буквами каждое слово."""
    return world.upper()


def string_title(world_1):
    """Функция принимает на вход строку и возвращает 
       заглавными первые буквы каждого слова."""
    return world_1.title()
